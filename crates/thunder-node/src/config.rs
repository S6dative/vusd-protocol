// LOAD / SAVE
// ─────────────────────────────────────────────────────────────────────────────

impl ThunderConfig {
    /// Load config from the default platform path.
    pub fn load() -> Result<Self, ConfigError> {
        Self::load_from(&config_file())
    }

    /// Load config from a specific path.
    pub fn load_from(path: &Path) -> Result<Self, ConfigError> {
        if !path.exists() {
            return Err(ConfigError::NotFound(path.to_path_buf()));
        }
        let content = fs::read_to_string(path)?;
        toml::from_str(&content)
            .map_err(|e| ConfigError::Parse(e.to_string()))
    }

    /// Save config to the default platform path, creating the directory if needed.
    pub fn save(&self) -> Result<(), ConfigError> {
        let path = config_file();
        if let Some(parent) = path.parent() {
            fs::create_dir_all(parent)?;
        }
        let content = toml::to_string_pretty(self)
            .map_err(|e| ConfigError::Parse(e.to_string()))?;
        fs::write(&path, content)?;
        Ok(())
    }

    /// Generate the default config file with all values commented and documented.
    /// This is what `thunder setup` writes to disk.
    pub fn generate_default_toml(onion_address: &str) -> String {
        format!(
            r#"# ⚡ Thunder Node — Configuration
# ════════════════════════════════════════════════════
# Generated by `thunder setup`. Edit as needed.
# Full documentation: https://github.com/S6dative/vusd-protocol

# Display name for this node (not used for routing — purely informational)
node_name = "Thunder Node"

[fees]
# Operator fee in basis points (100 = 1%, range 1–500)
# This is your cut of every transfer relayed through this node.
# Routes to your VUSD stealth wallet — not a Lightning address.
operator_fee_bps = 100

# Fee multiplier on standard Lightning base fee (1 = 1×, 2 = 2×)
# 2× signals to users that their transfer is wrapped in privacy infrastructure.
fee_multiplier = 2

# Your operator stealth wallet public keys (hex, 32 bytes each)
# Generate with: thunder setup --generate-wallet
# NEVER put your spend PRIVATE key here.
operator_spend_pubkey_hex = ""
operator_view_pubkey_hex  = ""

[lnd]
# LND gRPC endpoint (localhost because Tor handles the network)
endpoint       = "https://localhost:10009"
tls_cert_path  = "{lnd_dir}/tls.cert"
macaroon_hex   = ""  # xxd -p ~/.lnd/data/chain/bitcoin/signet/admin.macaroon | tr -d '\n'
timeout_secs   = 30

[tor]
# SOCKS5 proxy where Tor is listening
socks5_proxy    = "127.0.0.1:9050"
control_port    = 9051
# Refuse ALL non-Tor connections — set false only for local testing
clearnet_reject = true
# Your node's .onion address (run `cat /var/lib/tor/vultd-lnd/hostname`)
onion_address   = "{onion}"

# Relay peers — other Thunder Nodes you have private channels with
# Add one [[relays]] section per peer.
# [[relays]]
# pubkey_hex      = "aabbcc..."    # LND node pubkey (hex, 33 bytes)
# onion_address   = "abc123.onion" # relay's .onion address
# channel_id      = 123456789      # private channel ID (from `lncli listchannels`)
# max_forward_msat = 10000000      # max 10,000 sat per forward

[rotation]
# Rotate node identity keypair every N days
# Closes old channels and opens new ones under a new pubkey
enabled       = true
interval_days = 30

[logging]
# Log level: error | warn | info | debug | trace
level = "info"
# Uncomment to log to file:
# file = "/var/log/thunder-node.log"
"#,
            lnd_dir = if cfg!(windows) { "%APPDATA%\\Lnd" } else { "~/.lnd" },
            onion   = onion_address,
        )
    }
}

