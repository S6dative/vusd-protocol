/// Returns the platform-appropriate config directory.
///
/// - Linux/macOS: ~/.config/thunder-node/
/// - Windows:     %APPDATA%\thunder-node\
pub fn config_dir() -> PathBuf {
    let base = if cfg!(target_os = "windows") {
        std::env::var("APPDATA")
            .map(PathBuf::from)
            .unwrap_or_else(|_| dirs_home().join("AppData/Roaming"))
    } else if cfg!(target_os = "macos") {
        dirs_home().join("Library/Application Support")
    } else {
        std::env::var("XDG_CONFIG_HOME")
            .map(PathBuf::from)
            .unwrap_or_else(|_| dirs_home().join(".config"))
    };
    base.join("thunder-node")
}

pub fn config_file() -> PathBuf {
    config_dir().join("config.toml")
}

pub fn data_dir() -> PathBuf {
    if cfg!(target_os = "windows") {
        std::env::var("APPDATA")
            .map(PathBuf::from)
            .unwrap_or_else(|_| dirs_home().join("AppData/Roaming"))
            .join("thunder-node/data")
    } else if cfg!(target_os = "macos") {
        dirs_home().join("Library/Application Support/thunder-node/data")
    } else {
        std::env::var("XDG_DATA_HOME")
            .map(PathBuf::from)
            .unwrap_or_else(|_| dirs_home().join(".local/share"))
            .join("thunder-node")
    }
}

fn dirs_home() -> PathBuf {
    std::env::var("HOME")
        .or_else(|_| std::env::var("USERPROFILE"))
        .map(PathBuf::from)
        .unwrap_or_else(|_| PathBuf::from("/tmp"))
}

fn default_lnd_dir() -> PathBuf {
    if cfg!(target_os = "windows") {
        dirs_home().join("AppData/Local/Lnd")
    } else {
        dirs_home().join(".lnd")
    }
}

