// crates/thunder-node/src/config.rs
//
// Thunder Node — Configuration and Persistent State
// ═══════════════════════════════════════════════════
//
// Config file location (platform-appropriate):
//   Linux:   ~/.config/thunder-node/config.toml
//   macOS:   ~/Library/Application Support/thunder-node/config.toml
//   Windows: %APPDATA%\thunder-node\config.toml
//
// The config file is created by `thunder setup` with safe defaults.
// Operators edit it to configure relay nodes, fees, and key rotation schedule.

use std::path::{Path, PathBuf};
use std::fs;
use serde::{Deserialize, Serialize};
use thiserror::Error;

#[derive(Debug, Error)]
pub enum ConfigError {
    #[error("Config file not found at {0}. Run `thunder setup` to generate it.")]
    NotFound(PathBuf),
    #[error("Config parse error: {0}")]
    Parse(String),
    #[error("IO error: {0}")]
    Io(#[from] std::io::Error),
}

/// Top-level Thunder Node configuration file (config.toml).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ThunderConfig {
    /// Node identity (display name only — not used for routing)
    #[serde(default = "default_node_name")]
    pub node_name: String,

    /// Fee settings
    #[serde(default)]
    pub fees: FeeConfig,

    /// LND connection
    #[serde(default)]
    pub lnd: LndConnectionConfig,

    /// Tor settings
    #[serde(default)]
    pub tor: TorConnectionConfig,

    /// Relay peers (other Thunder/relay nodes)
    #[serde(default)]
    pub relays: Vec<RelayPeerConfig>,

    /// Key rotation
    #[serde(default)]
    pub rotation: RotationConfig,

    /// Logging
    #[serde(default)]
    pub logging: LogConfig,
}

fn default_node_name() -> String { "Thunder Node".to_string() }

impl Default for ThunderConfig {
    fn default() -> Self {
        ThunderConfig {
            node_name: default_node_name(),
            fees:      FeeConfig::default(),
            lnd:       LndConnectionConfig::default(),
            tor:       TorConnectionConfig::default(),
            relays:    vec![],
            rotation:  RotationConfig::default(),
            logging:   LogConfig::default(),
        }
    }
}

/// Fee configuration.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct FeeConfig {
    /// Operator cut in basis points (100 = 1%). Range: [1, 500].
    pub operator_fee_bps: u32,

    /// LN fee multiplier (2 = 2× standard LN fee). Range: [1, 5].
    pub fee_multiplier: u32,

    /// Operator stealth wallet spend public key (hex, 32 bytes).
    /// Generated by `thunder setup --generate-wallet`.
    /// The relay process only ever uses the view pubkey; the spend privkey
    /// lives on your air-gapped device.
    pub operator_spend_pubkey_hex: String,

    /// Operator stealth wallet view public key (hex, 32 bytes).
    pub operator_view_pubkey_hex: String,
}

impl Default for FeeConfig {
    fn default() -> Self {
        FeeConfig {
            operator_fee_bps:          100,   // 1%
            fee_multiplier:            2,     // 2× LN
            operator_spend_pubkey_hex: String::new(),
            operator_view_pubkey_hex:  String::new(),
        }
    }
}

/// LND gRPC connection.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LndConnectionConfig {
    /// LND gRPC endpoint. Default: localhost for Tor-only mode.
    pub endpoint: String,
    /// Path to LND TLS certificate.
    pub tls_cert_path: String,
    /// LND admin macaroon as a hex string (not a file path).
    /// Generate with: xxd -p ~/.lnd/data/chain/bitcoin/signet/admin.macaroon | tr -d '\n'
    pub macaroon_hex: String,
    /// Payment timeout in seconds.
    pub timeout_secs: u64,
}

impl Default for LndConnectionConfig {
    fn default() -> Self {
        let lnd_dir = default_lnd_dir();
        LndConnectionConfig {
            endpoint:       "https://localhost:10009".to_string(),
            tls_cert_path:  lnd_dir.join("tls.cert").display().to_string(),
            macaroon_hex:   String::new(), // set via: xxd -p ~/.lnd/.../admin.macaroon | tr -d '\n'
            timeout_secs:   30,
        }
    }
}

/// Tor configuration.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct TorConnectionConfig {
    /// SOCKS5 proxy address.
    pub socks5_proxy: String,
    /// Tor control port.
    pub control_port: u16,
    /// Reject any clearnet (non-Tor) connections.
    pub clearnet_reject: bool,
    /// Your node's .onion address (set after running `thunder setup`).
    pub onion_address: String,
}

impl Default for TorConnectionConfig {
    fn default() -> Self {
        TorConnectionConfig {
            socks5_proxy:    "127.0.0.1:9050".to_string(),
            control_port:    9051,
            clearnet_reject: true,
            onion_address:   String::new(),
        }
    }
}

/// A relay peer node.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RelayPeerConfig {
    /// Relay node's LND pubkey (hex, 33 bytes compressed secp256k1).
    pub pubkey_hex: String,
    /// Relay node's .onion address (v3).
    pub onion_address: String,
    /// Private channel ID to this relay (set after opening the channel).
    pub channel_id: u64,
    /// Max amount this relay will forward (millisatoshis).
    pub max_forward_msat: u64,
}

/// Key rotation configuration.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct RotationConfig {
    /// Enable automatic key rotation.
    pub enabled: bool,
    /// Rotation interval in days.
    pub interval_days: u32,
}

impl Default for RotationConfig {
    fn default() -> Self {
        RotationConfig { enabled: true, interval_days: 30 }
    }
}

/// Logging configuration.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LogConfig {
    /// Log level: error, warn, info, debug, trace
    pub level: String,
    /// Log to file (in addition to stdout)
    pub file: Option<String>,
}

impl Default for LogConfig {
    fn default() -> Self {
        LogConfig { level: "info".to_string(), file: None }
    }
}

