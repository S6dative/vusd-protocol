#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_default_config_serializes() {
        let cfg     = ThunderConfig::default();
        let toml    = toml::to_string_pretty(&cfg).unwrap();
        let parsed: ThunderConfig = toml::from_str(&toml).unwrap();
        assert_eq!(parsed.fees.operator_fee_bps, 100);
        assert_eq!(parsed.fees.fee_multiplier,   2);
        assert!(parsed.rotation.enabled);
    }

    #[test]
    fn test_generate_default_toml_contains_key_fields() {
        let toml = ThunderConfig::generate_default_toml("abc123.onion");
        assert!(toml.contains("operator_fee_bps = 100"));
        assert!(toml.contains("fee_multiplier = 2"));
        assert!(toml.contains("clearnet_reject = true"));
        assert!(toml.contains("abc123.onion"));
        assert!(toml.contains("interval_days = 30"));
    }

    #[test]
    fn test_config_roundtrip_with_relay() {
        let mut cfg = ThunderConfig::default();
        cfg.relays.push(RelayPeerConfig {
            pubkey_hex:       "aa".repeat(33),
            onion_address:    "test123.onion".to_string(),
            channel_id:       999_888_777,
            max_forward_msat: 5_000_000,
        });
        let toml: String = toml::to_string_pretty(&cfg).unwrap();
        let parsed: ThunderConfig = toml::from_str(&toml).unwrap();
        assert_eq!(parsed.relays.len(), 1);
        assert_eq!(parsed.relays[0].channel_id, 999_888_777);
        assert_eq!(parsed.relays[0].onion_address, "test123.onion");
    }

    #[test]
    fn test_config_dir_is_not_empty() {
        let dir = config_dir();
        assert!(dir.to_string_lossy().contains("thunder-node"));
    }

    #[test]
    fn test_data_dir_separate_from_config_dir() {
        let cfg  = config_dir();
        let data = data_dir();
        assert_ne!(cfg, data);
    }
}
